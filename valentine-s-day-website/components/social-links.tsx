export function SocialLinks({ links }: SocialLinksProps) {
  const [visible, setVisible] = useState(false)

  useEffect(() => {
    const timer = setTimeout(() => setVisible(true), 800)
    return () => clearTimeout(timer)
  }, [])

  return (
    <div className={`flex flex-col gap-3 w-full max-w-sm mx-auto transition-all duration-1000 ${visible ? "opacity-100 translate-y-0" : "opacity-0 translate-y-8"}`}>
      {links.map((link, index) => (
        <a
          key={index}
          href={link.url}
          target="_blank"
          rel="noopener noreferrer"
          className="group flex items-center gap-3 w-full px-4 py-3 rounded-xl bg-card/60 border border-border backdrop-blur-sm hover:border-primary hover:bg-primary/10 transition-all duration-300 neon-border"
          style={{ animationDelay: `${index * 0.1}s` }}
        >
          <span className="text-primary group-hover:scale-110 transition-transform">
            {icons[link.icon]}
          </span>
          <span className="text-sm font-medium text-foreground group-hover:text-primary transition-colors">
            {link.label}
          </span>
          <svg xmlns="http://www.w3.org/2000/svg" width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" className="ml-auto text-muted-foreground group-hover:text-primary group-hover:translate-x-1 transition-all">
            <path d="M5 12h14" />
            <path d="m12 5 7 7-7 7" />
          </svg>
        </a>
      ))}
    </div>
  )
}
